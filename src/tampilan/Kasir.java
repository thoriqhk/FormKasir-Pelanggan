    private void bhapus_kasirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapus_kasirActionPerformed
        int ok = JOptionPane.showConfirmDialog(null,"hapus","konfirmasi dialog",JOptionPane.YES_NO_OPTION);
        if (ok==0){
            String sql = "delete from kasir where id_kasir = '"+txtid_kasir.getText()+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
                kosong();
                txtid_kasir.requestFocus();
            }
            catch (SQLException e){
                JOptionPane.showMessageDialog(null, "data gagal dihapus"+e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapus_kasirActionPerformed

    private void bsimpan_kasirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpan_kasirActionPerformed
        String jenis = null;
        if(rlaki.isSelected()){
            jenis = "Laki-Laki";
        }else if(rperempuan.isSelected()){
            jenis = "Perempuan";
        }
        String sql = "insert into kasir values (?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtid_kasir.getText());
            stat.setString(2, txtnm_kasir.getText());
            stat.setString(3, jenis);
            stat.setString(4, txttelp_kasir.getText());
            stat.setString(5, txtalamat_kasir.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            txtid_kasir.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
        datatable();
    }//GEN-LAST:event_bsimpan_kasirActionPerformed

    private void bubah_kasirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubah_kasirActionPerformed
        String jenis = null;
        if(rlaki.isSelected()){
            jenis = "Laki-Laki";
        }else if(rperempuan.isSelected()){
            jenis = "Perempuan";
        }
        try{
            String sql = "update kasir set nama_kasir=?,jenis=?,telepon=?,alamat=? where id_kasir='"+txtid_kasir.getText()+"'";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtnm_kasir.getText());
            stat.setString(2, jenis);
            stat.setString(3, txttelp_kasir.getText());
            stat.setString(4, txtalamat_kasir.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil diubah");
            kosong();
            txtid_kasir.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal diubah"+e);
        }
        datatable();
    }//GEN-LAST:event_bubah_kasirActionPerformed

