    protected void datatable(){
        Object[] Baris ={"ID Kasir","Nama kasir","Jenis Kelamin","No.Telepon","Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcaridata_kasir.getText();
            try {
                String sql = "Select * FROM kasir where id_kasir like '%"+cariitem+"%' or nama_kasir like '%"+cariitem+"%' order by id_kasir asc";
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while (hasil.next()){
                    tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    });
                }
                tblkasir.setModel(tabmode);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
            }
    }

